    <div class="field field--name-body field--type-text-with-summary field--label-hidden"><div class="field__items"><div class="field__item even" property="content:encoded"><p>A <strong>privacy notice</strong> (also sometimes referred to as a privacy policy) is a key document which you must have if you collect, use or process personal data of European Union citizens.</p>

<p>Under the <strong>EU General Data Protection Regulation (GDPR)</strong>, you must provide this document:</p>

<ul><li>to inform people how you collect, process and use their personal data</li>
	<li>typically at the point of data collection</li>
	<li>in plain and clear language, accessible format, and free of charge</li>
</ul><p>The GDPR sets out the specific information you must supply to individuals and when.</p>

<h3>How to write a GDPR privacy notice?</h3>

<p>If you collect personal data from the individuals themselves, you must include the following in your privacy notice at the time you obtain the data:</p>

<ul><li>the data controller's identity and contact details</li>
	<li>details of your data protection officer (if you are required to have one)</li>
	<li>the purpose and legal basis for data processing</li>
	<li>where the legal basis for processing is legitimate interest, what that interest is</li>
	<li>where the legal basis is consent, the right to withdraw consent at any time</li>
	<li>the existence of individual's rights (known as data subject rights)</li>
	<li>with whom you will share personal data (named parties or categories of recipients)</li>
	<li>whether you plan to transfer data to third countries and what safeguards will exist</li>
	<li>how long you will keep the personal data for (or details of your retention criteria)</li>
	<li>the right to lodge a complaint with the Information Commissioner's Office</li>
	<li>if there is a statutory or contractual requirement for the data subject to provide personal data, and if so, the consequences of failing to provide data</li>
	<li>if you intend to carry out any automated decision making (eg profiling), how you will make these decisions, their significance and possible consequences</li>
</ul><p>In addition to the above, if you collect data <strong>from a third party</strong> (ie from a source other than the data subject), you must also include in the privacy notice:</p>

<ul><li>categories of personal data concerned</li>
	<li>the source of data (and whether it came from publicly available sources</li>
</ul><p>Your privacy notice will usually sit on your website. You should link to it when asking people to eg subscribe to your newsletter, register with your service or provide you any personal information in any other way.</p>

